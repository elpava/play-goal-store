const PRODUCTS_LENGTH = DUMMY_DATA.length

DUMMY_DATA.sort((a, b) => {
  if (a.brand < b.brand) return -1
  if (a.brand > b.brand) return 1
  return 0
})
const filteredByBrand = DUMMY_DATA.map(data => data.brand)
const uniqueBrand = [...new Set(filteredByBrand)]
const brandsAnchors = uniqueBrand.map(brand => ({
  name: brand,
  href: `#${brand}`,
}))

const groupedData = {}
DUMMY_DATA.forEach(item => {
  const { brand } = item

  if (!groupedData[brand]) {
    groupedData[brand] = { brandName: brand, products: [] }
  }
  groupedData[brand].products.push(item)
})
const groupedDataArray = Object.values(groupedData)

let randomPositions = []
let x = 0
while (x < PRODUCTS_LENGTH / 1.25) {
  randomPositions.push(randomNumber())
  x++
}
randomPositions = [...new Set(randomPositions)]

function randomNumber() {
  return Math.floor(Math.random() * PRODUCTS_LENGTH)
}

const randomAddedProductsToCart = randomPositions.map(position => ({
  _id: DUMMY_DATA[position]._id,
  name: DUMMY_DATA[position].name,
  thumbnail: DUMMY_DATA[position].thumbnails[0],
  quantity: Math.trunc(randomNumber() / 4) + 1,
  stock: DUMMY_DATA[position].stock,
}))
const randomAddedProductsIds = randomPositions.map(
  position => DUMMY_DATA[position]._id,
)

export {
  DUMMY_DATA,
  brandsAnchors,
  groupedDataArray,
  randomAddedProductsIds,
  randomAddedProductsToCart,
}
